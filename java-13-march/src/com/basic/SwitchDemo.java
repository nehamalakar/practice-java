package com.basic;

public class SwitchDemo {
public static void main(String[] args) {
	int choice=4;
	
	switch (choice) {
	case 1:
		System.out.println("sunday");
		break;
	case 2:
		System.out.println("monday");
		break;
	case 3:
		System.out.println("tuesday");
		break;
	case 4:
		System.out.println("wednesday");
		break;
	case 5:
		System.out.println("thursday");
		break;
	default:
		break;
	}
	
}
}
